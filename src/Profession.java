/**
 * Інтерфейс, що представляє професію.
 * Професія визначається методом performDuties(), який виконує обов'язки, пов'язані з цією професією.
 */
public interface Profession {

    /**
     * Метод для виконання обов'язків, пов'язаних з даною професією.
     * Реалізація цього методу в конкретних класах-реалізаторах інтерфейсу повинна визначати
     * конкретні дії, які виконуються працівником даної професії.
     */
    void performDuties();
}
